-- NovaWave Business ERP - schema inicial PostgreSQL 15+
-- Destinado a bancos novos. Alteracoes futuras devem usar migrations versionadas.

BEGIN;

-- =========================================================
-- PLATAFORMA E SAAS
-- =========================================================

CREATE TABLE public.platform_usuario (
  idplatform_usuario integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nome varchar(150) NOT NULL,
  email varchar(150) NOT NULL,
  senha_hash varchar(255) NOT NULL,
  telefone varchar(30),
  cargo varchar(100),
  nivel_acesso varchar(20) NOT NULL DEFAULT 'super_admin',
  avatar_url varchar(255),
  ultimo_login timestamp,
  situacao smallint NOT NULL DEFAULT 1,
  criado_em timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  atualizado_em timestamp,
  CONSTRAINT ck_platform_usuario_situacao CHECK (situacao IN (0, 1)),
  CONSTRAINT ck_platform_usuario_nivel CHECK (
    nivel_acesso IN ('super_admin', 'suporte', 'financeiro', 'comercial', 'leitura')
  ),
  CONSTRAINT uq_platform_usuario_email UNIQUE (email)
);

CREATE UNIQUE INDEX uq_platform_usuario_email_normalizado
  ON public.platform_usuario (LOWER(BTRIM(email)));

CREATE TABLE public.platform_login_tentativa (
  idtentativa integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  email varchar(150) NOT NULL,
  ip varchar(50),
  sucesso boolean NOT NULL DEFAULT false,
  criado_em timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_platform_login_bloqueio
  ON public.platform_login_tentativa (LOWER(BTRIM(email)), ip, criado_em)
  WHERE sucesso = false;

CREATE TABLE public.platform_configuracao (
  idconfiguracao integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  chave varchar(100) NOT NULL,
  valor jsonb NOT NULL DEFAULT '{}'::jsonb,
  grupo varchar(50) NOT NULL,
  segredo boolean NOT NULL DEFAULT false,
  atualizado_por integer,
  criado_em timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  atualizado_em timestamp,
  CONSTRAINT fk_platform_config_usuario FOREIGN KEY (atualizado_por)
    REFERENCES public.platform_usuario(idplatform_usuario)
    ON UPDATE CASCADE ON DELETE SET NULL,
  CONSTRAINT uq_platform_config_chave UNIQUE (chave)
);

CREATE TABLE public.plano (
  idplano integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nome varchar(100) NOT NULL,
  descricao varchar(500),
  valor_mensal numeric(10,2) NOT NULL DEFAULT 0,
  valor_anual numeric(10,2) NOT NULL DEFAULT 0,
  limite_usuarios integer,
  limite_filiais integer,
  limite_produtos integer,
  limite_armazenamento_mb integer,
  modulos jsonb NOT NULL DEFAULT '{}'::jsonb,
  situacao smallint NOT NULL DEFAULT 1,
  criado_em timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  atualizado_em timestamp,
  CONSTRAINT ck_plano_valores CHECK (valor_mensal >= 0 AND valor_anual >= 0),
  CONSTRAINT ck_plano_limites CHECK (
    (limite_usuarios IS NULL OR limite_usuarios >= 0) AND
    (limite_filiais IS NULL OR limite_filiais >= 0) AND
    (limite_produtos IS NULL OR limite_produtos >= 0) AND
    (limite_armazenamento_mb IS NULL OR limite_armazenamento_mb >= 0)
  ),
  CONSTRAINT ck_plano_situacao CHECK (situacao IN (0, 1)),
  CONSTRAINT uq_plano_nome UNIQUE (nome)
);

CREATE TABLE public.empresa (
  idempresa integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  razao_social varchar(150) NOT NULL,
  nome_fantasia varchar(150) NOT NULL,
  cnpj varchar(18),
  inscricao_estadual varchar(30),
  email varchar(150),
  telefone varchar(30),
  cep varchar(20),
  endereco varchar(150),
  numero varchar(20),
  complemento varchar(100),
  bairro varchar(100),
  cidade varchar(100),
  estado varchar(2),
  logo_url varchar(255),
  favicon_url varchar(255),
  cor_primaria varchar(7) NOT NULL DEFAULT '#2F80FF',
  cor_secundaria varchar(7) NOT NULL DEFAULT '#4F46E5',
  timezone varchar(60) NOT NULL DEFAULT 'America/Sao_Paulo',
  moeda varchar(3) NOT NULL DEFAULT 'BRL',
  idioma varchar(10) NOT NULL DEFAULT 'pt-BR',
  situacao smallint NOT NULL DEFAULT 1,
  criado_em timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  atualizado_em timestamp,
  CONSTRAINT ck_empresa_situacao CHECK (situacao IN (0, 1, 2)),
  CONSTRAINT ck_empresa_cores CHECK (
    cor_primaria ~ '^#[0-9A-Fa-f]{6}$' AND cor_secundaria ~ '^#[0-9A-Fa-f]{6}$'
  ),
  CONSTRAINT uq_empresa_cnpj UNIQUE (cnpj),
  CONSTRAINT uq_empresa_idempresa UNIQUE (idempresa)
);

CREATE TABLE public.assinatura (
  idassinatura integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  idempresa integer NOT NULL,
  idplano integer NOT NULL,
  data_inicio date NOT NULL,
  data_fim date,
  data_proxima_cobranca date,
  status smallint NOT NULL DEFAULT 2,
  forma_pagamento varchar(50),
  valor_atual numeric(10,2) NOT NULL DEFAULT 0,
  criado_em timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  atualizado_em timestamp,
  CONSTRAINT fk_assinatura_empresa FOREIGN KEY (idempresa)
    REFERENCES public.empresa(idempresa) ON UPDATE CASCADE ON DELETE RESTRICT,
  CONSTRAINT fk_assinatura_plano FOREIGN KEY (idplano)
    REFERENCES public.plano(idplano) ON UPDATE CASCADE ON DELETE RESTRICT,
  CONSTRAINT ck_assinatura_status CHECK (status IN (1, 2, 3, 4, 5)),
  CONSTRAINT ck_assinatura_valor CHECK (valor_atual >= 0),
  CONSTRAINT ck_assinatura_datas CHECK (data_fim IS NULL OR data_fim >= data_inicio)
);

-- =========================================================
-- ESTRUTURA ORGANIZACIONAL
