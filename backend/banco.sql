CREATE TABLE public.filial (
  idfilial integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  idempresa integer NOT NULL,
  nome varchar(150) NOT NULL,
  codigo varchar(50) NOT NULL,
  cnpj varchar(18),
  inscricao_estadual varchar(30),
  email varchar(150),
  telefone varchar(30),
  cep varchar(20),
  endereco varchar(150),
  numero varchar(20),
  complemento varchar(100),
  bairro varchar(100),
  cidade varchar(100),
  estado varchar(2),
  matriz boolean NOT NULL DEFAULT false,
  idgerente integer,
  latitude numeric(9,6),
  longitude numeric(9,6),
  permite_estoque_negativo boolean NOT NULL DEFAULT false,
  caixa_obrigatorio boolean NOT NULL DEFAULT true,
  situacao smallint NOT NULL DEFAULT 1,
  criado_em timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  atualizado_em timestamp,
  CONSTRAINT fk_filial_empresa FOREIGN KEY (idempresa)
    REFERENCES public.empresa(idempresa) ON UPDATE CASCADE ON DELETE RESTRICT,
  CONSTRAINT ck_filial_situacao CHECK (situacao IN (0, 1)),
  CONSTRAINT ck_filial_coordenadas CHECK (
    (latitude IS NULL OR latitude BETWEEN -90 AND 90) AND
    (longitude IS NULL OR longitude BETWEEN -180 AND 180)
  ),
  CONSTRAINT uq_filial_empresa_idfilial UNIQUE (idempresa, idfilial),
  CONSTRAINT uq_filial_empresa_codigo UNIQUE (idempresa, codigo),
  CONSTRAINT uq_filial_empresa_nome UNIQUE (idempresa, nome)
);

CREATE UNIQUE INDEX uq_filial_matriz_empresa
  ON public.filial(idempresa) WHERE matriz = true;

CREATE TABLE public.departamento (
  iddepartamento integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  idempresa integer NOT NULL,
  nome varchar(100) NOT NULL,
  descricao varchar(255),
  situacao smallint NOT NULL DEFAULT 1,
  criado_em timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  atualizado_em timestamp,
  CONSTRAINT fk_departamento_empresa FOREIGN KEY (idempresa)
    REFERENCES public.empresa(idempresa) ON UPDATE CASCADE ON DELETE RESTRICT,
  CONSTRAINT ck_departamento_situacao CHECK (situacao IN (0, 1)),
  CONSTRAINT uq_departamento_empresa_id UNIQUE (idempresa, iddepartamento),
  CONSTRAINT uq_departamento_empresa_nome UNIQUE (idempresa, nome)
);

CREATE TABLE public.cargo (
  idcargo integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  idempresa integer NOT NULL,
  iddepartamento integer,
  nome varchar(100) NOT NULL,
  descricao varchar(255),
  salario_base numeric(10,2) NOT NULL DEFAULT 0,
  situacao smallint NOT NULL DEFAULT 1,
  criado_em timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  atualizado_em timestamp,
  CONSTRAINT fk_cargo_empresa FOREIGN KEY (idempresa)
    REFERENCES public.empresa(idempresa) ON UPDATE CASCADE ON DELETE RESTRICT,
  CONSTRAINT fk_cargo_departamento FOREIGN KEY (idempresa, iddepartamento)
    REFERENCES public.departamento(idempresa, iddepartamento)
    ON UPDATE CASCADE ON DELETE RESTRICT,
  CONSTRAINT ck_cargo_salario CHECK (salario_base >= 0),
  CONSTRAINT ck_cargo_situacao CHECK (situacao IN (0, 1)),
  CONSTRAINT uq_cargo_empresa_id UNIQUE (idempresa, idcargo),
  CONSTRAINT uq_cargo_empresa_nome UNIQUE (idempresa, nome)
);

CREATE TABLE public.funcionario (
  idfuncionario integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  idempresa integer NOT NULL,
  idfilial integer NOT NULL,
  idcargo integer,
  nome varchar(150) NOT NULL,
  cpf varchar(14),
  email varchar(150),
  telefone varchar(30),
  salario numeric(10,2) NOT NULL DEFAULT 0,
  data_admissao date,
  data_demissao date,
  situacao smallint NOT NULL DEFAULT 1,
  criado_em timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  atualizado_em timestamp,
  CONSTRAINT fk_funcionario_empresa FOREIGN KEY (idempresa)
    REFERENCES public.empresa(idempresa) ON UPDATE CASCADE ON DELETE RESTRICT,
  CONSTRAINT fk_funcionario_filial FOREIGN KEY (idempresa, idfilial)
    REFERENCES public.filial(idempresa, idfilial) ON UPDATE CASCADE ON DELETE RESTRICT,
  CONSTRAINT fk_funcionario_cargo FOREIGN KEY (idempresa, idcargo)
    REFERENCES public.cargo(idempresa, idcargo) ON UPDATE CASCADE ON DELETE RESTRICT,
  CONSTRAINT ck_funcionario_salario CHECK (salario >= 0),
  CONSTRAINT ck_funcionario_datas CHECK (
    data_demissao IS NULL OR data_admissao IS NULL OR data_demissao >= data_admissao
  ),
  CONSTRAINT ck_funcionario_situacao CHECK (situacao IN (0, 1)),
  CONSTRAINT uq_funcionario_empresa_id UNIQUE (idempresa, idfuncionario),
  CONSTRAINT uq_funcionario_empresa_cpf UNIQUE (idempresa, cpf)
);

CREATE TABLE public.usuario (
  idusuario integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  idempresa integer NOT NULL,
  idfilial_padrao integer,
  idfuncionario integer,
  nome varchar(150) NOT NULL,
  email varchar(150) NOT NULL,
  senha_hash varchar(255) NOT NULL,
  telefone varchar(30),
  avatar_url varchar(255),
  admin_empresa boolean NOT NULL DEFAULT false,
  ultimo_login timestamp,
  situacao smallint NOT NULL DEFAULT 1,
  criado_em timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  atualizado_em timestamp,
  CONSTRAINT fk_usuario_empresa FOREIGN KEY (idempresa)
    REFERENCES public.empresa(idempresa) ON UPDATE CASCADE ON DELETE RESTRICT,
  CONSTRAINT fk_usuario_filial_padrao FOREIGN KEY (idempresa, idfilial_padrao)
    REFERENCES public.filial(idempresa, idfilial) ON UPDATE CASCADE ON DELETE RESTRICT,
  CONSTRAINT fk_usuario_funcionario FOREIGN KEY (idempresa, idfuncionario)
    REFERENCES public.funcionario(idempresa, idfuncionario)
    ON UPDATE CASCADE ON DELETE SET NULL (idfuncionario),
  CONSTRAINT ck_usuario_situacao CHECK (situacao IN (0, 1)),
  CONSTRAINT uq_usuario_empresa_id UNIQUE (idempresa, idusuario),
  CONSTRAINT uq_usuario_empresa_email UNIQUE (idempresa, email),
  CONSTRAINT uq_usuario_empresa_funcionario UNIQUE (idempresa, idfuncionario)
);

-- O login inicial usa apenas e-mail e senha; portanto o e-mail precisa identificar
-- inequivocamente um usuario entre todos os tenants.
CREATE UNIQUE INDEX uq_usuario_email_normalizado
  ON public.usuario (LOWER(BTRIM(email)));

ALTER TABLE public.filial
  ADD CONSTRAINT fk_filial_gerente
  FOREIGN KEY (idempresa, idgerente)
  REFERENCES public.usuario(idempresa, idusuario)
  ON UPDATE CASCADE ON DELETE SET NULL (idgerente);

